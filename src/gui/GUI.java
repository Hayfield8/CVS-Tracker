    private void createVomitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_createVomitMouseClicked
        // TODO add your handling code here:
        vomitedString = vomitCombo.getSelectedItem().toString();
        if ("yes".equals(vomitedString)){
            cvsTrackerCoord.aVomited = true;
        }
        else {
            cvsTrackerCoord.aVomited = false;
        }

        cvsTrackerCoord.aNoOfVomit = Integer.parseInt(noOfVomitField.getText());

        violentString = violentCombo.getSelectedItem().toString();
        if ("yes".equals(violentString)){
            cvsTrackerCoord.aViolent = true;
        }
        else {
            cvsTrackerCoord.aViolent = false;
        }

        eatenString = eatenCombo.getSelectedItem().toString();
        if ("yes".equals(eatenString)){
                cvsTrackerCoord.aEatenBefore = true;
        }
        else {
            cvsTrackerCoord.aEatenBefore = false;
        }

        cvsTrackerCoord.aEatenWhat = eatenWhatField.getText();
        
        drunkString = drunkCombo.getSelectedItem().toString();
        if ("yes".equals(drunkString)){
            cvsTrackerCoord.aDrunkAlcohol = true;
        }
        else {
            cvsTrackerCoord.aDrunkAlcohol = false;
        }
        
        cvsTrackerCoord.aDrunkWhat = drunkWhatField.getText();
        
        hungoverString = hungoverCombo.getSelectedItem().toString();
        if ("yes".equals(hungoverString)){
            cvsTrackerCoord.aHungover = true;
        }
        else {
            cvsTrackerCoord.aHungover = false;
        }
        
        cvsTrackerCoord.aVomit = new Vomit(cvsTrackerCoord.aVomited, 
        cvsTrackerCoord.aNoOfVomit, cvsTrackerCoord.aViolent, 
        cvsTrackerCoord.aEatenBefore, cvsTrackerCoord.aEatenWhat, 
        cvsTrackerCoord.aDrunkAlcohol, cvsTrackerCoord.aDrunkWhat,
        cvsTrackerCoord.aHungover);

        infoStoredDialog.pack();
        infoStoredDialog.setVisible(true);
        infoStoredDialog.setLocation(500, 500);

    }//GEN-LAST:event_createVomitMouseClicked

