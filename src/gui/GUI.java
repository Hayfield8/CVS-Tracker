    /**
     * Creates a new instance of the CvsEvent class and adds it to the event list
     * if the user has created a Vomit and Condition object otherwise it prompts
     * the user to create them when they have clicked the button.
     * @see CvsEvent
     * @param evt 
     */
    private void createEventMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_createEventMouseClicked
        // TODO add your handling code here:
        
        cvsTrackerCoord.aEventBeginTime = EventBeginTime.valueOf(
        beginTimeCombo.getSelectedItem().toString());
        
        cvsTrackerCoord.aLocation = Location.valueOf(
        locationCombo.getSelectedItem().toString());
        
        cvsTrackerCoord.aHoursLasted = Integer.parseInt(hoursLastedField.getText
        ());
        
        cvsTrackerCoord.aDaysLasted = Integer.parseInt(daysLastedField.getText
        ());
        
        cvsTrackerCoord.aNotes = notesArea.getText();
        
        if (cvsTrackerCoord.aVomit != null && cvsTrackerCoord.aCondition != null)
        {
            cvsTrackerCoord.aCvsEvent = new CvsEvent (cvsTrackerCoord.aEventBeginTime,
                    cvsTrackerCoord.aLocation, cvsTrackerCoord.aHoursLasted,
                    cvsTrackerCoord.aDaysLasted, cvsTrackerCoord.aNotes,
                    cvsTrackerCoord.aVomit, cvsTrackerCoord.aCondition);
            
            cvsTrackerCoord.events.add(cvsTrackerCoord.aCvsEvent);
            
            
            model.addElement(cvsTrackerCoord.aCvsEvent);
            
            eventCreatedDialog.pack();
            eventCreatedDialog.setVisible(true);
            eventCreatedDialog.setLocation(500, 500);
        }
        else
        {
            createEventError.pack();
            createEventError.setVisible(true);
            createEventError.setLocation(500, 500);
        }
        
        
    }//GEN-LAST:event_createEventMouseClicked

