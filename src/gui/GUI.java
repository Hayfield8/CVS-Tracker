    /**
     * Create a new instance of the Condition class when the user has clicked the 
     * button.
     * @see Condition
     * @param evt 
     */
    private void createConditionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_createConditionMouseClicked
        // 
        
        anxiousString = anxiousCombo.getSelectedItem().toString();
        if ("yes".equals(anxiousString)){
            cvsTrackerCoord.aAnxious = true;
        }
        else {
            cvsTrackerCoord.aAnxious = false;
        }
        
        cvsTrackerCoord.aMood = Mood.valueOf(moodCombo.getSelectedItem().
        toString());
        
        areYouIllString = areYouIllCombo.getSelectedItem().toString();
        if ("yes".equals(areYouIllString)){
            cvsTrackerCoord.aAreYouIll = true;
        }
        else {
            cvsTrackerCoord.aAreYouIll = false;
        }
        
        cvsTrackerCoord.aIllWithWhat = illWithWhatField.getText();
        
        sleptString = sleptCombo.getSelectedItem().toString();
        if ("yes".equals(sleptString)){
            cvsTrackerCoord.aSlept = true;
        }
        else {
            cvsTrackerCoord.aSlept = false;
        }
        
        cvsTrackerCoord.aHrsSlept = Integer.parseInt(hrsSleptField.getText());
        
        cvsTrackerCoord.aMedicationTaken = medicationTakenArea.getText();
        
        cvsTrackerCoord.aCondition = new Condition(cvsTrackerCoord.aAnxious, 
        cvsTrackerCoord.aMood, cvsTrackerCoord.aAreYouIll,
        cvsTrackerCoord.aIllWithWhat, cvsTrackerCoord.aSlept, 
        cvsTrackerCoord.aHrsSlept, cvsTrackerCoord.aMedicationTaken);
        
        infoStoredDialog.pack();
        infoStoredDialog.setVisible(true);
        infoStoredDialog.setLocation(500, 500);
        
        
        
            
    }//GEN-LAST:event_createConditionMouseClicked

